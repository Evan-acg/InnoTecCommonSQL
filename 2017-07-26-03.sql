SELECT T1.SOHNUM_0 N'事业部订单号',
		T1.SOPLIN_0 N'订单行号',
		T1.SALFCY_0 N'事业部',
		T3.BPCORD_0 N'客户代码',
		T1.ITMREF_0 N'产品名称',
		T1.QTY_0 N'产品数量',
		T1.DLVQTYSTU_0 N'发货数量',
		T3.BPCNAM_0 N'客户名称',
		T1.CREDAT_0 N'订单日期'
FROM SORDERQ T1
	LEFT JOIN ITMFACILIT T2 ON T1.ITMREF_0 = T2.ITMREF_0 AND T1.SALFCY_0 = T2.STOFCY_0
	LEFT JOIN ZSOYN T3 ON T1.SOHNUM_0 = T3.XSOHNUM_0 AND T1.SOPLIN_0 = T3.XSOPLIN_0
WHERE T2.REOCOD_0 <> 3
	AND T1.CREDAT_0 > '2017-05-01'
	AND T1.SALFCY_0 <> '0101'


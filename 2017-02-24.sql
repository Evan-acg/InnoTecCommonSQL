SELECT T1.MFGNUM_0 AS'工单号',
		T2.ITMREF_0 AS'总成编码',
		T2.STU_0 AS'单位',
		T2.EXTQTY_0 AS'生产数量',
		T3.ITMDES1_0 AS'产品名称',
		T1.ITMREF_0 AS'组件编码',
		(SELECT ITMDES1_0 FROM ITMMASTER WHERE ITMREF_0 = T1.ITMREF_0) AS'组件名称',
		T1.STU_0 AS'单位',
		T1.RETQTY_0 AS'需求数量',
		T1.ALLQTY_0 AS'已指派数量',
		(T1.RETQTY_0 - T1.ALLQTY_0) AS'未转移数量'
FROM MFGMAT T1
	LEFT JOIN MFGITM T2 ON T1.MFGNUM_0 = T2.MFGNUM_0
	LEFT JOIN ITMMASTER T3 ON T1.ITMREF_0 = T3.ITMREF_0
WHERE (T1.RETQTY_0 - T1.ALLQTY_0) <> 0



SELECT T1.ITMREF_0,
		T1.ITMDES1_0,
		T1.QTY_0,
		T1.STU_0,
		T3.ZCUSNUM_0,
		T1.SOHNUM_0,
		T3.ZRMK_0,
		T3.FCY_0,
		T7.BPCORD_0,
		T7.BPCNAM_0,
		T1.CREDAT_0,
		T1.SDHNUM_0,
		T4.TEL_0,
		T4.FAX_0,
		T4.WEB_0,
		T4.MOB_0,
		T5.CNTLNA_0 + CNTFNA_0 CNTLNA_0,
		T5.CNTETS_0,
		T5.CNTFAX_0,
		T5.CNTMOB_0,
		T6.BPAADDLIG_0
FROM SDELIVERYD T1
	LEFT JOIN SORDER T7 ON T1.SOHNUM_0 = T7.SOHNUM_0
	LEFT JOIN SDELIVERY T2 ON T1.SDHNUM_0 = T2.SDHNUM_0
	LEFT JOIN ZSOYN1 T3 ON T1.SOHNUM_0 = T3.SOHNUM_0 AND T1.SOPLIN_0 = T3.SOPLIN_0
	LEFT JOIN (SELECT BPANUM_0,CCNCRM_0,TEL_0,FAX_0,WEB_0,MOB_0 FROM CONTACT) T4 ON T7.BPCORD_0 = T4.BPANUM_0
	LEFT JOIN (SELECT CNTNUM_0,CNTLNA_0,CNTFNA_0,CNTETS_0,CNTFAX_0,CNTMOB_0 FROM CONTACTCRM WHERE LEFT(CNTNUM_0,1) <> 'S') T5 ON T4.CCNCRM_0 = T5.CNTNUM_0
	LEFT JOIN BPADDRESS T6 ON T7.BPCORD_0 = T6.BPANUM_0

WHERE T1.SDHNUM_0 = '0101SDH18030001'



































SELECT T1.MFGTRKNUM_0,
		T1.MATTRKLIN_0,
		T1.ITMREF_0,
		T2.ITMREF_0,
		T1.USEQTY_0,
		T1.STU_0,
		T1.IPTDAT_0,
		T1.ITMREF_0,
		T1.MFGNUM_0,
		T1.MFGLIN_0,
		T1.CREDAT_0,
		T1.MFGFCY_0
FROM MFGMATTRK T1
	LEFT JOIN MFGITM T2 ON T1.MFGNUM_0 = T2.MFGNUM_0 AND T2.MFGLIN_0 = 1000
WHERE T1.IPTDAT_0 BETWEEN '2017/01/01' AND '2017/06/30'
ORDER BY T1.MFGTRKNUM_0,T1.MATTRKLIN_0
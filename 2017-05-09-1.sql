SELECT T1.NUM_0 AS XSDH,
		T1.ACCDAT_0 AS DJRQ,
		T1.BPYNAM_0 AS KHMC,
		T2.CRN_0 AS KHSH,
		T1.BPYADDLIG_0+T3.TEL_0 AS KHDZDH,
		T4.PAB1_0+T4.BIDNUM_0 AS KHYHZH,
        ISNULL(T1.DES_0,'')+''+ISNULL(T1.DES_1,'')+''+ISNULL(T1.DES_2,'') AS BZ
FROM SINVOICE T1 
	LEFT JOIN BPARTNER T2 ON T1.BPR_0=T2.BPRNUM_0
	LEFT JOIN BPADDRESS T3 ON T2.BPRNUM_0=T3.BPANUM_0
	LEFT JOIN BID T4 ON T2.BPRNUM_0=T4.BPANUM_0
WHERE T1.XINVSTA_0='1' AND T1.ORIMOD_0='5'
SELECT			T1.STOFCY_0,
				T1.ITMREF_0,
				T2.ITMDES1_0,
				T3.LANMES_0,
				T2.STU_0
FROM ITMFACILIT T1
	LEFT JOIN ITMMASTER T2 ON T1.ITMREF_0 = T2.ITMREF_0
	LEFT JOIN APLSTD T3 ON T1.REOCOD_0 = T3.LANNUM_0 AND LAN_0 = N'CHI' AND LANCHP_0 = N'250'
WHERE T1.STOFCY_0 IN(N'0201',N'0202',N'0203')
ORDER BY T1.STOFCY_0,T1.ITMREF_0


SELECT TOP 100 * FROM ITMFACILIT 
SELECT TOP 100 * FROM APLSTD WHERE LAN_0 = N'CHI' AND LANCHP_0 = N'250'
		T1.ITMREF_0,
		T2.ITMDES1_0,
		(T4.RETQTY_0 / T3.EXTQTY_0) LIKQTY_0,
		T4.RETQTY_0,
		ISNULL(T1.USEQTY_0,0) QTY_0,
		T2.STU_0,
		T4.USEQTY_0,
		T1.MFGNUM_0,
		T1.MFGFCY_0,

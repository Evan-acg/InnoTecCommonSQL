		T6.SOHNUM_0
 FROM MFGMATTRK T1
	LEFT JOIN ITMMASTER T2 ON T1.ITMREF_0 = T2.ITMREF_0
	LEFT JOIN MFGITM T3 ON T1.MFGNUM_0 = T3.MFGNUM_0 AND T3.MFGLIN_0 = 1000
	LEFT JOIN MFGMAT T4 ON T1.MFGNUM_0= T4.MFGNUM_0
							AND T1.MFGLIN_0 = T4.MFGLIN_0
							AND T1.ITMREF_0 = T4.ITMREF_0
							AND T1.BOMSEQ_0 = T4.BOMSEQ_0
	LEFT JOIN ZSOYN T5 ON T3.VCRNUMORI_0 = T5.SOHNUM_0
							AND T3.VCRLINORI_0 = T5.SOPLIN_0
	LEFT JOIN ZSOYN1 T6 ON T3.VCRNUMORI_0 = T6.XSOHNUM_0
							AND T3.VCRLINORI_0 = T6.XSOPLIN_0
WHERE T1.MFGTRKNUM_0 = '0202MTK17071217'


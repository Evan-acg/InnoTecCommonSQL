SELECT T1.SOHNUM_0 N'销售订单号',
		T1.SOPLIN_0 N'销售订单行号',
		T4.BPCNAM_0 N'客户名称',
		T1.ITMREF_0 N'产品号',
		T1.QTY_0 N'订单数量'
FROM SORDERQ T1
	LEFT JOIN SDELIVERYD T2 ON T1.SOHNUM_0 = T2.SOHNUM_0
							 AND T1.SOPLIN_0 = T2.SOPLIN_0
	LEFT JOIN SORDER T3 ON T1.SOHNUM_0 = T3.SOHNUM_0
	LEFT JOIN BPCUSTOMER T4 ON T1.BPCORD_0 = T4.BPCNUM_0
WHERE T2.SDHNUM_0 IS NULL
	AND(T1.CREUSR_0 IN('0031','0078')
		OR T3.REP_0 IN('D003','D114',''))

SELECT * FROM AUTILIS ORDER BY USR_0
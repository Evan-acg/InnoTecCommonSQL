SELECT	T1.MFGNUM_0 N'工单号',
		T3.ITMREF_0 N'成品',
		T1.ITMREF_0 N'组件',
		T4.ITMDES1_0 N'名称',
		T1.RETQTY_0 N'总需求',
		T1.SHTQTY_0 N'总短缺',
		T3.STRDAT_0 N'开始日期',
		T5.BPCNAM_0 N'客户名称'
FROM MFGMAT T1
	LEFT JOIN MFGHEAD T2 ON T1.MFGNUM_0 = T2.MFGNUM_0
	LEFT JOIN MFGITM T3 ON T1.MFGNUM_0 = T3.MFGNUM_0 AND T1.MFGFCY_0 = T3.MFGFCY_0
	LEFT JOIN ITMMASTER T4 ON T1.ITMREF_0 = T4.ITMREF_0
	LEFT JOIN ZSOYN T5 ON T3.VCRNUMORI_0 = T5.XSOHNUM_0 AND T3.VCRLINORI_0 = XSOPLIN_0
WHERE T1.MATSTA_0 <> 4
	AND T3.MFGLIN_0 = 1000
	AND T2.MFGSTA_0 <> 4





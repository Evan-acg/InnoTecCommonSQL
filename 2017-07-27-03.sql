		T1.CREDAT_0,
		T1.BPCINV_0,
		T1.ITMREF_0,
		T1.ITMDES1_0,
		T1.QTY_0,
		T1.STU_0,
		T1.NETPRI_0,
		T1.NETPRINOT_0,
		T1.PFM_0,
		T1.CPRPRI_0,
		T1.AMTNOTLIN_0,
		T1.AMTATILIN_0,
		T2.CUR_0,
		T1.STOFCY_0,
		T2.PTE_0,
		T1.SOHNUM_0,
		T1.SDHNUM_0
FROM SINVOICED T1
	LEFT JOIN SINVOICE T2 ON T1.NUM_0 = T2.NUM_0
WHERE T1.CREDAT_0 < '2017-07-01'
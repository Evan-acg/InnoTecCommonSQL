SELECT T1.VCRNUM_0 N'单号',
		T1.ITMREF_0 N'产品代码',
		T1.ITMDES1_0 N'产品名称',
		T1.STU_0 N'单位',
		(ISNULL(T1.QTYPCU_0,0) - ISNULL(T4.QTY_0,0)) N'盘盈数量',
		T3.MATTOT_0 N'成本',
		T1.QTYPCU_0 * T3.MATTOT_0  N'金额'
FROM SMVTD T1
	LEFT JOIN SMVTH T2 ON T1.VCRNUM_0 = T2.VCRNUM_0
	LEFT JOIN ITMCOST T3 ON T1.ITMREF_0 = T3.ITMREF_0 AND T3.STOFCY_0 = '0203' AND T3.ITCSEQ_0 = (SELECT MAX(ITCSEQ_0) FROM ITMCOST)
	LEFT JOIN (SELECT A.ITMREF_0,
				SUM(B.CPLQTY_0 * (A.RETQTY_0/B.EXTQTY_0) - A.USEQTY_0) QTY_0
		 FROM MFGMAT A,MFGITM B 
		 WHERE (A.RETQTY_0 - A.USEQTY_0) <> 0 
			 AND A.MATSTA_0 = 3 
			 AND A.MFGNUM_0 = B.MFGNUM_0 
			 AND B.MFGLIN_0 = 1000
			 AND A.MFGFCY_0 = '0203'
		 GROUP BY A.ITMREF_0,(B.CPLQTY_0 * (A.RETQTY_0/B.EXTQTY_0) - A.USEQTY_0)
			 ) T4 ON T1.ITMREF_0 = T4.ITMREF_0
WHERE T2.TRSCOD_0 = 'R02'
	AND T1.CREUSR_0 = 'ALAN'
	AND T1.CREDAT_0 >= '2017-05-20'
	AND T2.STOFCY_0 = '0203'


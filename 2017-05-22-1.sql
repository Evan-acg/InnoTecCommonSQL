SELECT T1.CODINT_0,
		T2.TEXTE_0
FROM AFONCTION T1
	LEFT JOIN ATEXTE T2 ON T1.CODINT_0 = T2.NOMOBJ_0
WHERE T2.LAN_0 = 'CHI'

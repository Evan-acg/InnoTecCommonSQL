

SELECT *
FROM (SELECT PRFCOD_0,FNC_0 FROM AFCTPRF WHERE PRFCOD_0 = 'YMIGA') T1
	FULL JOIN (SELECT PRFCOD_0,FNC_0 FROM AFCTPRF WHERE PRFCOD_0 = 'YSTOA') T2 ON T1.FNC_0 = T2.FNC_0
WHERE T1.PRFCOD_0 IS NULL OR T2.PRFCOD_0 IS NULL




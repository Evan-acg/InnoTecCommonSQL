SELECT T1.MFGTRKNUM_0,
		T1.MFGNUM_0,
		T1.MFGLIN_0,
		T1.ITMREF_0,
		T1.USEQTY_0,
		T2.ITMREF_0,
		T2.EXTQTY_0,
		T3.SOHNUM_0,
		T4.POHNUM_0,
		T5.SOHNUM_0,
		T5.BPCORD_0,
		T6.BPCNAM_0,
		T7.LIKQTY_0
FROM MFGMATTRK T1
	LEFT JOIN MFGITM T2 ON T1.MFGNUM_0 = T2.MFGNUM_0
	LEFT JOIN SORDERQ T3 ON T2.VCRNUMORI_0 = T3.SOHNUM_0 AND T2.VCRLINORI_0 = T3.SOPLIN_0
	LEFT JOIN PORDERQ T4 ON T3.POHNUM_0 = T4.POHNUM_0 AND T3.POPLIN_0 = T4.POPLIN_0
	LEFT JOIN SORDERQ T5 ON T4.POHNUM_0 = T5.FMINUM_0 AND T4.POPLIN_0 = T5.FMILIN_0
	LEFT JOIN BPCUSTOMER T6 ON T5.BPCORD_0 = T6.BPCNUM_0
	LEFT JOIN MFGMAT T7 ON T1.MFGNUM_0 = T7.MFGNUM_0 AND T1.ITMREF_0 = T7.ITMREF_0 AND T1.BOMSEQ_0 = T7.BOMSEQ_0
WHERE T1.MFGTRKNUM_0 = '0201MTK17060371'







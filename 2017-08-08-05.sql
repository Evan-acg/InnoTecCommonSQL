		T1.ITMREF_0 N'产品编码',
		T1.ITMDES1_0 N'产品名称',
		T1.SDHNUM_0 N'发货单号',
		T1.QTY_0 N'发货数量',
		T1.GROPRI_0 N'单价',
		T1.QTY_0 * T1.GROPRI_0 N'合计',
		T1.CREDAT_0 N'发货日期'
FROM SDELIVERYD T1
	LEFT JOIN SDELIVERY T2 ON T1.SDHNUM_0 = T2.SDHNUM_0
WHERE T1.SDHNUM_0 = '0101SDH17080168'
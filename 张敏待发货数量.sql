SELECT T1.SOHNUM_0 N'销售订单号',
		T1.BPCORD_0 N'客户代码',
		T2.BPCNAM_0 N'客户名称',
		T1.ITMREF_0 N'产品代码',
		T4.ITMDES1_0 N'产品名称',
		T1.QTY_0 N'订单数量',
		T1.DLVQTY_0 N'已发货数量',
		(T1.QTY_0 - T1.DLVQTY_0) N'待运送数量',
		T1.DEMDLVDAT_0 N'要求发货日期',
		LEFT(T3.XSOHNUM_0,4) N'所属事业部'
FROM SORDERQ T1
	LEFT JOIN SORDER T2 ON T1.SOHNUM_0 = T2.SOHNUM_0
	LEFT JOIN ZSOYN T3 ON T1.SOHNUM_0 = T3.SOHNUM_0 AND T1.SOPLIN_0 = T3.SOPLIN_0
	LEFT JOIN ITMMASTER T4 ON T1.ITMREF_0 = T4.ITMREF_0
WHERE T1.SOQSTA_0 <> 3
	AND (T1.QTY_0 - T1.DLVQTY_0) > 0
	AND T1.SALFCY_0 = '0101'
	AND T1.ITMREF_0 LIKE 'M%'
ORDER BY T1.DEMDLVDAT_0


SELECT TOP 1000 * FROM ZSOYN
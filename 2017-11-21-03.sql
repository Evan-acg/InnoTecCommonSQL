SELECT T1.CREDAT_0 N'下单日期',
		T2.POHNUM_0 N'采购单号',
		T2.XSOHNUM_0 N'事业部销售单号',
		T1.ITMREF_0 N'产品编码',
		T2.ITMDES1_0 N'产品名称',
		T1.QTY_0 N'数量',
		T3.NETPRIATI_0 N'含税单价',
		T1.QTY_0*T3.NETPRIATI_0 N'含税总价',
		T4.DLVQTY_0 N'已经缴库数量',
		T4.QTY_0-T4.DLVQTY_0 N'未缴库数量',
		T6.CREDAT_0 N'最新缴库日期',
		T4.SALFCY_0 N'所属事业部',
		T5.NOMUSR_0 N'制单人'
FROM SORDERQ T1 
	LEFT JOIN ZSOYN T2 ON T1.SOHNUM_0 = T2.SOHNUM_0 AND T1.SOPLIN_0 = T2.SOPLIN_0 
	LEFT JOIN SORDERP T3 ON T1.SOHNUM_0 = T3.SOHNUM_0 AND T1.SOPLIN_0 = T3.SOPLIN_0 
	LEFT JOIN SORDERQ T4 ON T2.XSOHNUM_0 = T4.SOHNUM_0 AND T2.XSOPLIN_0 = T4.SOPLIN_0 
	LEFT JOIN AUTILIS T5 ON T1.CREUSR_0 = T5.USR_0 
	LEFT JOIN (SELECT SOHNUM_0,SOPLIN_0,MAX(CREDAT_0) CREDAT_0 FROM SDELIVERYD GROUP BY SOHNUM_0,SOPLIN_0,CREDAT_0) T6 ON T2.XSOHNUM_0 = T6.SOHNUM_0 AND T2.XSOPLIN_0 = T6.SOPLIN_0 
	LEFT JOIN PORDERQ T7 ON T2.POHNUM_0 = T7.POHNUM_0 AND T2.POPLIN_0 = T7.POPLIN_0 
WHERE T4.QTY_0 - T4.DLVQTY_0 > 0 
	AND T7.CREUSR_0 IN(N'0032',N'0078')
	AND T4.SOHNUM_0 = '0203SOH17050281'
ORDER BY T1.CREDAT_0


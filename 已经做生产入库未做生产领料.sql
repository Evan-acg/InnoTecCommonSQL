SELECT 	T1.MFGTRKNUM_0 N'追踪单号',
		T1.MFGNUM_0 N'工单号',
		T2.ITMREF_0 N'成品',
		T1.ITMREF_0 N'组件',
		T1.CREDAT_0 N'追踪日期',
		T3.CREDAT_0 N'生产入库日期'
FROM (SELECT * FROM MFGMATTRK WHERE CREDAT_0 > '2017-07-01') T1
	LEFT JOIN MFGITM T2 ON T1.MFGNUM_0 = T2.MFGNUM_0
			AND T1.MFGFCY_0 = T2.MFGFCY_0
			AND T2.MFGLIN_0 = '1000'
	LEFT JOIN MFGITMTRK T3 ON T1.MFGNUM_0 = T3.MFGNUM_0
			AND T1.MFGLIN_0 = T3.MFGLIN_0
			AND T1.BOMNUM_0 = T3.ITMREF_0
WHERE T3.CREDAT_0 <  '2017-07-01'




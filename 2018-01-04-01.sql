DECLARE @itemName nvarchar(100)
SET @itemName = N'IFT7669'
SELECT T1.VCRNUM_0,T1.VCRLIN_0,T1.ITMREF_0,T1.QTYSTU_0,T2.LOT_0
FROM SMVTD T1
	LEFT JOIN STOJOU T2 ON T1.VCRNUM_0 = T2.VCRNUM_0 AND T1.VCRLIN_0 = T2.VCRLIN_0
WHERE T1.ITMREF_0 = @itemName AND T1.CREDAT_0 BETWEEN N'2017-10-30' AND GETDATE() AND T1.CREUSR_0 = N'ADMIN' AND LEFT(T1.VCRNUM_0,4) = N'0201'
ORDER BY T2.LOT_0


















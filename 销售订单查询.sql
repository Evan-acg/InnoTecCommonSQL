SELECT A.POHNUM_0,
  A.POPLIN_0,
  B.SOHNUM_0,
  B.SOPLIN_0,
  C.SOHNUM_0,
  C.SOPLIN_0,
  B.BPCORD_0,
  D.BPCNAM_0,
  B.ITMREF_0,
  E.ITMDES1_0,
  C.QTYSTU_0,
  C.DLVQTY_0,
  B.ZCUSNUM_0,
  B.ZRMK_0,
  C.DEMDLVDAT_0,
  C.CREDAT_0,
  (CASE F.REOCOD_0 WHEN 1 THEN N'无提议' WHEN 2 THEN N'采购' WHEN 3 THEN N'生产' WHEN 4 THEN N'地点' WHEN 5 THEN N'外协'END) N'产品属性'
FROM PORDERQ A,SORDERQ B,SORDERQ C,BPCUSTOMER D,ITMMASTER E,ITMFACILIT F
WHERE (A.SOHNUM_0 = B.SOHNUM_0 AND A.SOPLIN_0 = B.SOPLIN_0) 
AND (C.POHNUM_0 = A.POHNUM_0 AND C.POPLIN_0 = A.POPLIN_0)
AND (D.BPCNUM_0 = B.BPCORD_0 AND B.ITMREF_0 = E.ITMREF_0)
AND (C.SALFCY_0 = F.STOFCY_0 AND C.ITMREF_0 = F.ITMREF_0)
AND A.POHNUM_0 = '0101POH17090164'


SELECT		  T1.POHNUM_0,
			  T1.POPLIN_0,
			  T2.SOHNUM_0,
			  T2.SOPLIN_0,
			  T3.SOHNUM_0,
			  T3.SOPLIN_0,
			  T2.BPCORD_0,
			  T4.BPCNAM_0,
			  T2.ITMREF_0,
			  T5.ITMDES1_0,
			  T3.QTYSTU_0,
			  T3.DLVQTY_0,
			  T2.ZCUSNUM_0,
			  T2.ZRMK_0,
			  T3.DEMDLVDAT_0,
			  T3.CREDAT_0,
			  (CASE T6.REOCOD_0 WHEN 1 THEN N'无提议' WHEN 2 THEN N'采购' WHEN 3 THEN N'生产' WHEN 4 THEN N'地点' WHEN 5 THEN N'外协'END) N'产品属性' 
FROM PORDERQ T1
	LEFT JOIN SORDERQ T2 ON T1.SOHNUM_0 = T2.SOHNUM_0 AND T1.SOPLIN_0 = T2.SOPLIN_0
	LEFT JOIN SORDERQ T3 ON T3.POHNUM_0 = T1.POHNUM_0 AND T3.POPLIN_0 = T1.POPLIN_0
	LEFT JOIN BPCUSTOMER T4 ON T4.BPCNUM_0 = T2.BPCORD_0
	LEFT JOIN ITMMASTER T5 ON T3.ITMREF_0 = T1.ITMREF_0
	LEFT JOIN ITMFACILIT T6 ON T3.ITMREF_0 = T6.ITMREF_0 AND T2.ITMREF_0 = T6.ITMREF_0
WHERE T1.POHNUM_0 = '0101POH17090164'

